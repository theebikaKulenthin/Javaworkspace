package workspace;

public class ArithmeticOperators {
	public static void main(String args[])
	{
		int n1=100,n2=10;
		System.out.println("n1+n2:"+(n1+n2));
		System.out.println("n1-n2:"+(n1-n2));
		System.out.println("n1*n2:"+(n1*n2));
		System.out.println("n1/n2:"+(n1/n2));
		System.out.println("n1%n2:"+(n1%n2));
	}
}
