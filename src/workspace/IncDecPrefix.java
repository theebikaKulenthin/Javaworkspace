package workspace;

public class IncDecPrefix {
	public static void main(String[] args)
	{
		int a=10,b=20;
		int result1,result2;
		System.out.println("a:"+a);
		result1=++a;
		System.out.println("increment:"+result1);
		System.out.println("b:"+b);
		result2=--b;
		System.out.println("decrement:"+result2);
	}

}
